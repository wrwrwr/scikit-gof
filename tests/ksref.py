"""
A collection of reference values for the Kolmogorov-Smirnov two-sided,
one-sample, distribution-free statistic.
"""
from fractions import Fraction as F
from math import sqrt

from skgof.ksdist import ks_unif_durbin_recurrence_rational as reference


def print_reference(samples_min, samples_max, samples_step):
    """
    Prepares a testing reference using an exact rational arithmetic.

    The printed values are as accurate as the language float type allows.
    The tuples are (sample count, statistic value, Pr(D <= st), P(D > st)).
    """
    for samples in range(samples_min, samples_max, samples_step):
        for factor in (1 / 5, 1 / 4, 1 / 3, 1 / 2, 1, 2, 3, 4, 5):
            statistic = F.from_float(.868731160636 / sqrt(samples) * factor)
            probability = reference(samples, statistic)
            print('    ({}, {}, {}, {}),'.format(samples, float(statistic),
                                float(probability), float(1 - probability)))


